function format(number) {
	switch (globals.format) {
		case "short":
			return shortFormat(number);
		case "alphabet":
			return alphabetFormat(number);
		case "scientific":
			return scientificFormat(number);
		case "engineering":
			return engineeringFormat(number);
	}
}

